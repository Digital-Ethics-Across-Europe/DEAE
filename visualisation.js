function updateMapColors(year) {
  const paths = document.querySelectorAll("svg path");

  paths.forEach((path) => {
    const countryId = path.id;
    if (data[year] && data[year][countryId]) {
      path.style.fill = data[year][countryId].color;
    } else {
      path.style.fill = "#f5f3f4";
    }
  });
}


